def update_province_descriptions():
    """更新省份描述信息"""
    app = create_app()
    
    with app.app_context():
        # 获取所有省份
        provinces = Province.objects()
        print(f"数据库中共有 {provinces.count()} 个省份")
        
        updated_count = 0
        
        for province in provinces:
            # 检查是否需要更新描述
            if province.name in PROVINCE_DESCRIPTIONS:
                new_description = PROVINCE_DESCRIPTIONS[province.name]
                
                # 如果描述不同则更新
                if province.description != new_description:
                    old_description = province.description
                    province.description = new_description
                    province.save()
                    print(f"✓ 已更新 {province.name} 的描述")
                    print(f"  原描述: {old_description}")
                    print(f"  新描述: {new_description}")
                    updated_count += 1
                else:
                    print(f"- {province.name} 的描述已是最新的")
            else:
                print(f"! 未找到 {province.name} 的描述信息")
        
        print(f"\n处理完成! 共更新了 {updated_count} 个省份的描述信息")

def main():
    """主函数"""
    print("省份描述更新工具")
    print("=" * 50)
    print("此工具将为数据库中的所有省份添加详细的介绍信息")
    print("=" * 50)
    
    update_province_descriptions()

if __name__ == "__main__":
    main()